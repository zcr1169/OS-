#include "系统模拟器.h"
#include <cstdlib>
#ifdef _WIN32
#include <windows.h>
#endif

/**
 * main — 程序入口
 *
 * 流程:
 *   1. 设置控制台为 UTF-8 编码（解决 Windows 中文乱码）
 *   2. 创建模拟器实例
 *   3. init() 初始化: 加载持久化状态 or 创建 init 进程
 *   4. run() 进入主循环: 读用户输入 → 执行命令 → 输出结果
 *
 * 注意: 整个程序只有一个主线程在 main 中跑前台循环，
 *       后台线程和调度线程在 run() 内部启动。
 */
int main() {
    // ---- Windows 控制台 UTF-8 设置 ----
    // Windows 默认控制台编码是 GBK，打印中文会乱码
    // 这里把代码页切到 65001 (UTF-8)
#ifdef _WIN32
    system("chcp 65001 > nul 2>&1");
    SetConsoleOutputCP(CP_UTF8);
#endif

    OSSimulator simulator;          // 创建模拟器（构造函数里计算数据目录路径）

    simulator.init();               // 初始化: 尝试加载存档，没有则创建 init 进程
    simulator.run();                // 进入前台交互主循环（直到用户输入 exit）

    return 0;
}
